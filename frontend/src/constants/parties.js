// ── Helper functions ──────────────────────────────────────────────────
export function partyFlag(name) {
  return PARTY_FLAGS[name] || null;
}

export function partyColor(name) {
  return PARTY_COLORS[name] || PARTY_COLORS['default'];
}

export function partyEn(name) {
  return PARTY_EN[name] || (name && name.length > 30 ? name.substring(0, 28) + '…' : name) || 'Independent';
}

export function partyShort(name) {
  return PARTY_SHORT[name] || (name ? name.replace(/[()]/g, '').split(' ').map(w => w[0]).join('').substring(0, 5).toUpperCase() : '?');
}

// ── PartyFlag component — shows flag with fallback to colored badge ────
// Usage: <PartyFlag name={partyName} size={24} />
import React from 'react';

export function PartyFlag({ name, size = 28, className = '', style = {} }) {
  const flag = partyFlag(name);
  const color = partyColor(name);
  const short = partyShort(name);

  const [imgError, setImgError] = React.useState(false);

  if (flag && !imgError) {
    return (
      <img
        src={flag}
        alt={partyEn(name)}
        onError={() => setImgError(true)}
        className={className}
        style={{
          width: size,
          height: size * 0.65,
          objectFit: 'cover',
          borderRadius: 3,
          flexShrink: 0,
          border: '1px solid rgba(0,0,0,0.1)',
          ...style,
        }}
      />
    );
  }

  // Fallback: colored badge with short name
  return (
    <div
      className={className}
      style={{
        width: size,
        height: size * 0.65,
        background: color,
        borderRadius: 3,
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        fontSize: Math.max(7, size * 0.22),
        fontWeight: 800,
        color: 'white',
        flexShrink: 0,
        letterSpacing: '0.3px',
        ...style,
      }}
    >
      {short.substring(0, 3)}
    </div>
  );
}